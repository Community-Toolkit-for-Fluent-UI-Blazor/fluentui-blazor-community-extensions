namespace FluentUI.Blazor.Community.Components;

/// <summary>
/// Represents the direction of the items of a <see cref="FluentCxSleekDial"/>
/// </summary>
public enum SleekDialLinearDirection
{
    /// <summary>
    /// Default direction from the position of the <see cref="FluentCxSleekDial"/>.
    /// </summary>
    Default,

    /// <summary>
    /// Direction to the up.
    /// </summary>
    Up,

    /// <summary>
    /// Direction to the down.
    /// </summary>
    Down,

    /// <summary>
    /// Direction to the left.
    /// </summary>
    Left,

    /// <summary>
    /// Direction to the right.
    /// </summary>
    Right
}
