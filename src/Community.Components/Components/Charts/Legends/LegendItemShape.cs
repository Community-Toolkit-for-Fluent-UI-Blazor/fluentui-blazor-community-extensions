namespace FluentUI.Blazor.Community.Components;

/// <summary>
/// Specifies the shape used to represent a legend item in a chart or graphical legend.
/// </summary>
/// <remarks>Use this enumeration to select the visual shape displayed for legend items, such as markers or
/// symbols, in chart legends. The available shapes provide options for customizing the appearance of legend entries to
/// match the style or semantics of the chart data.</remarks>
public enum LegendItemShape
{
    /// <summary>
    /// Represents a circle shape.
    /// </summary>
    Circle,

    /// <summary>
    /// Represents a square shape.
    /// </summary>
    Square,

    /// <summary>
    /// Represents a rounded square shape.
    /// </summary>
    RoundSquare,

    /// <summary>
    /// Represents an ellipse shape.
    /// </summary>
    Ellipse,

    /// <summary>
    /// Represents a diamond shape.
    /// </summary>
    Diamond,

    /// <summary>
    /// Represents a chevron shape.
    /// </summary>
    Chevron
}
