namespace FluentUI.Blazor.Community.Components;

/// <summary>
/// Represents the types of grids that can be displayed in the signature component.
/// </summary>
public enum SignatureGridType
{
    /// <summary>
    /// No grid is displayed.
    /// </summary>
    None,

    /// <summary>
    /// Represents a grid made up of lines.
    /// </summary>
    Lines,

    /// <summary>
    /// Represents a grid made up of dots.
    /// </summary>
    Dots
}
