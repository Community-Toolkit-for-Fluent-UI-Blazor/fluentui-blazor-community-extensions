namespace FluentUI.Blazor.Community.Components;

/// <summary>
/// Represents the available animations for opening and closing the <see cref="FluentCxSleekDial"/>.
/// </summary>
public enum SleekDialAnimation
{
    /// <summary>
    /// None animation.
    /// </summary>
    None,

    /// <summary>
    /// Fade animation.
    /// </summary>
    Fade,

    /// <summary>
    /// Fade and zoom animation.
    /// </summary>
    FadeZoom,

    /// <summary>
    /// Flip left and down animation.
    /// </summary>
    FlipLeftDown,

    /// <summary>
    /// Flip left and up animation.
    /// </summary>
    FlipLeftUp,

    /// <summary>
    /// Flip down and right animation.
    /// </summary>
    FlipRightDown,

    /// <summary>
    /// Flip right and up animation.
    /// </summary>
    FlipRightUp,

    /// <summary>
    /// Flip on X axis and down animation.
    /// </summary>
    FlipXDown,

    /// <summary>
    /// Flip on X axis and up animation.
    /// </summary>
    FlipXUp,

    /// <summary>
    /// Flip on Y axis and left animation.
    /// </summary>
    FlipYLeft,

    /// <summary>
    /// Flip on Y axis and right animation.
    /// </summary>
    FlipYRight,

    /// <summary>
    /// Slide from top to bottom animation.
    /// </summary>
    SlideBottom,

    /// <summary>
    /// Slide from right to left animation.
    /// </summary>
    SlideLeft,

    /// <summary>
    /// Slide from left to right animation.
    /// </summary>
    SlideRight,

    /// <summary>
    /// Slide from bottom to top animation.
    /// </summary>
    SlideTop,

    /// <summary>
    /// Zoom animation.
    /// </summary>
    Zoom
}
